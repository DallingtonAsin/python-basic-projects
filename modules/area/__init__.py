import math



def square(side):
    return side * side

def rectangle(width, height):
    return width * height

def circle(radius):
    return 2 * math.pi * radius

def triangle(base, height):
    return 0.5 * base * height

def cube(side):
    return 6 * side**2

def sphere(radius):
    return 4 * math.pi * radius ** 2